package com.covalense.javaapp.stream;

public class Student {
	String name;
	double percentage;

	public Student(String name, double percentage) {
		super();
		this.name = name;
		this.percentage = percentage;
	}

	public String getName() {
		return name;
	}

	public double getPercentage() {
		return percentage;
	}

}
